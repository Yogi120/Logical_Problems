using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace LogicalProblems
{
    public class Conditions
    {
        public double Absolute(double num)
        {
            if (num >= 0)
            {
                return num = num;
            }
            else
            {
                return num = num * -1;
            }
        }
    }
}
